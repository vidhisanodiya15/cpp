// C++ program to demonstrate 
// the long modifier
#include <iostream>
using namespace std;

// Driver Code
int main()
{
	cout << "Size of long int : " << 
			sizeof(long int) << 
			" bytes" << endl;
	cout << "Size of long double : " << 
			sizeof(long double) << 
			" bytes" << endl;
	return 0;
}

